def test_hwp2py_can_skip_binary_assets() -> None:
    script_text = generate_hwp_script(HWP_SAMPLE, include_binary_assets=False)

    assert "base64" not in script_text
    assert "doc.append_ole(" not in script_text
    assert "Skipped OLE asset" in script_text


def test_hwp2py_cli_writes_script(tmp_path: Path) -> None:
    script_path = tmp_path / "source_to_py.py"

    result = hwp2py_main(
        [
            str(HWP_SAMPLE),
            "-o",
            str(script_path),
            "--default-output",
            "cli-output.hwp",
            "--skip-binary-assets",
        ]
    )

    assert result == 0
    script_text = script_path.read_text(encoding="utf-8")
    assert "_DEFAULT_OUTPUT = 'cli-output.hwp'" in script_text
    assert "def build_document() -> HancomDocument:" in script_text
    assert "def write_hwp(path: str | Path = _DEFAULT_OUTPUT) -> Path:" in script_text
    assert "Skipped OLE asset" in script_text


